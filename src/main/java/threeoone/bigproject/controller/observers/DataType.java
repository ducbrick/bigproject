package threeoone.bigproject.controller.observers;

/**
 * The {@code DataType} enum represents different types of data that can be observed.
 * It is used to distinguish between various data types in the observer pattern.
 */
public enum DataType {
  /**
   * Represents member-related data.
   */
  MEMBER,

  /**
   * Represents document-related data.
   */
  DOCUMENT
}
