package threeoone.bigproject.controller;

/**
 * Enumeration representing different scene names in the application.
 */
public enum SceneName {

  /**
   * Scene representing the document overview.
   */
  DOC_OVERVIEW,

  /**
   * Scene for getting the name.
   */
  GET_NAME,

  /**
   * Scene representing the document detail view.
   */
  DOC_DETAIL,

  /**
   * Scene for adding a new document.
   */
  ADD_DOC
}
