package threeoone.bigproject.controller.viewcontrollers;

/**
 * The {@code DocQueryType} enum represents different query types for documents.
 * It is used to specify the type of query being performed on documents.
 *
 * @author HUY1902
 */
public enum DocQueryType {
  /**
   * Represents a query by document ID.
   */
  ID,

  /**
   * Represents a query by document name.
   */
  NAME,

  /**
   * Represents a query by document author.
   */
  AUTHOR,

  /**
   * Represents a query by document category.
   */
  CATEGORY
}
